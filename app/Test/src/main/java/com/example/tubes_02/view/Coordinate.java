package com.example.tubes_02.view;

//This probably should go to model.

public class Coordinate {
    float x;
    float y;

    public Coordinate(float x, float y){
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }
}
